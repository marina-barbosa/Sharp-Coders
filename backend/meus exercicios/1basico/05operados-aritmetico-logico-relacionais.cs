

// Operadores Aritméticos:

// Os operadores aritméticos realizam operações matemáticas em variáveis numéricas.

// + (Adição):
int soma = 5 + 3; // Resultado: 8



// - (Subtração):
int diferenca = 10 - 4; // Resultado: 6



// * (Multiplicação):
int produto = 2 * 6; // Resultado: 12



// / (Divisão):
int quociente = 8 / 2; // Resultado: 4



// % (Módulo - Resto da Divisão):
int resto = 10 % 3; // Resultado: 1



// Operadores Relacionais:

// Os operadores relacionais comparam valores e retornam um valor booleano.


// == (Igual a):
bool igual = (5 == 5); // Resultado: true



// != (Diferente de):
bool diferente = (5 != 3); // Resultado: true



// < (Menor que):
bool menorQue = (4 < 7); // Resultado: true



// > (Maior que):
bool maiorQue = (8 > 5); // Resultado: true



// <= (Menor ou igual a):
bool menorOuIgual = (6 <= 6); // Resultado: true



// >= (Maior ou igual a):
bool maiorOuIgual = (9 >= 8); // Resultado: true



// Operadores Lógicos:

// Os operadores lógicos são usados para realizar operações lógicas em valores booleanos.


// && (E lógico):
bool resultadoE = (true && false); // Resultado: false



// || (OU lógico):
bool resultadoOu = (true || false); // Resultado: true



// ! (NÃO lógico):
bool resultadoNao = !true; // Resultado: false


