using System;

class Program
{
    // Método principal - ponto de entrada do programa
    static void Main()
    {
        // Corpo do método principal
        Console.WriteLine("Olá, mundo!"); // Exemplo de saída no console

        // Aguarda a entrada do usuário antes de fechar a aplicação
        Console.ReadLine();
    }
}

// Aqui está uma breve explicação dos elementos-chave:

// using System;
// Esta linha indica que o programa está utilizando o namespace System. Os namespaces são usados para organizar e fornecer um escopo para as classes e métodos em C#. O System contém classes fundamentais que são frequentemente usadas, como a classe Console.

// class Program
// Define uma classe chamada Program. Em C#, o código é organizado em classes, e o método Main é geralmente o ponto de entrada do programa.

// static void Main()
// Este é o método principal do programa. O termo static significa que o método pertence à classe em vez de uma instância específica. O void indica que o método não retorna nenhum valor. O Main é o ponto de entrada onde a execução do programa começa.

// Console.WriteLine("Olá, mundo!");
// Demonstração de uma instrução de saída no console. O método WriteLine exibe o texto entre as aspas duplas no console.

// Console.ReadLine();
// Aguarda a entrada do usuário antes de fechar a aplicação. Isso é adicionado para que o console não feche imediatamente após a exibição da mensagem.

// Ao executar este programa, você verá "Olá, mundo!" impresso no console.
